class FoodItem {
  final String id;
  final String name;
  final String image;
  final double price;
  final double rating;
  final int time;
  final String description;

  FoodItem({
    required this.id,
    required this.name,
    required this.image,
    required this.price,
    required this.rating,
    required this.time,
    required this.description,
  });
}
